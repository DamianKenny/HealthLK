export const getDistricts = (province: string): string[] => {
  if (province === "All Provinces") {
    return ["All Districts", ...Array.from(new Set(facilities.map(f => f.district))).sort()];
  }
  const districts = Array.from(new Set(
    facilities.filter(f => f.province === province).map(f => f.district)
  )).sort();
  return ["All Districts", ...districts];
};

